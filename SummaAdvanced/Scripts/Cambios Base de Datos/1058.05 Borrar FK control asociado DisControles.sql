DECLARE @ConstraintName nvarchar(200)
SELECT 
    @ConstraintName = KCU.CONSTRAINT_NAME
FROM INFORMATION_SCHEMA.REFERENTIAL_CONSTRAINTS AS RC 
INNER JOIN INFORMATION_SCHEMA.KEY_COLUMN_USAGE AS KCU
    ON KCU.CONSTRAINT_CATALOG = RC.CONSTRAINT_CATALOG  
    AND KCU.CONSTRAINT_SCHEMA = RC.CONSTRAINT_SCHEMA 
    AND KCU.CONSTRAINT_NAME = RC.CONSTRAINT_NAME
WHERE
    KCU.TABLE_NAME = 'disControlesBoton' AND
    KCU.COLUMN_NAME = 'ControlAsociado_Id'

IF @ConstraintName IS NOT NULL EXEC('alter table disControlesBoton drop  CONSTRAINT ' + @ConstraintName)
GO


INSERT INTO genVersion(Version) Values(1058.05)
GO
