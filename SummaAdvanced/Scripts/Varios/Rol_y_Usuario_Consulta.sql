USE [NOMBRE_BASE_DATOS]
CREATE ROLE RolConsulta;
GO

GRANT CREATE VIEW TO RolConsulta;
GO

GRANT SELECT ON SCHEMA::dbo TO RolConsulta;
GO

GRANT ALTER ON SCHEMA::dbo TO RolConsulta;
GO


CREATE LOGIN Consulta WITH PASSWORD=N'Consulta', DEFAULT_DATABASE=NOMBRE_BASE_DATOS, CHECK_EXPIRATION=OFF, CHECK_POLICY=ON
GO

CREATE USER [Consulta] FOR LOGIN [Consulta]
GO

EXEC sp_addrolemember RolConsulta, Consulta
GO

